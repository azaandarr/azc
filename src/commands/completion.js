// completion.js — `azc completion` command.
// Generates shell completion scripts for bash, zsh, and fish.
// Users source the output in their shell profile:
//   source <(azc completion bash)
//   source <(azc completion zsh)
//   azc completion fish | source

const path = require('path');
const chalk = require('chalk');
const config = require('../config/config');

const REGIONS = [
  'uksouth', 'ukwest', 'westeurope', 'northeurope',
  'eastus', 'eastus2', 'westus2', 'centralus',
  'southeastasia', 'eastasia', 'australiaeast',
];

const COMMANDS = 'scan price compare plan config doctor completion';
const GLOBAL_FLAGS = '--quiet --no-color --verbose --version --help';

/**
 * List dynamic completable values.
 * Called internally by shell scripts via `azc completion --list <type>`.
 */
function listValues(type) {
  if (type === 'subscriptions') {
    const cfg = config.loadConfig();
    const subs = Object.keys(cfg.subscriptions || {});
    subs.forEach((s) => process.stdout.write(s + '\n'));
  } else if (type === 'services') {
    const mappings = require(path.join(__dirname, '../../data/sku-mappings.json'));
    const aliases = new Set();
    for (const entry of Object.values(mappings.services)) {
      entry.aliases.forEach((a) => aliases.add(a));
    }
    [...aliases].sort().forEach((a) => process.stdout.write(a + '\n'));
  } else if (type === 'regions') {
    REGIONS.forEach((r) => process.stdout.write(r + '\n'));
  }
}

/**
 * Generate bash completion script.
 */
function generateBash() {
  const regions = REGIONS.join(' ');
  return `# azc bash completion — generated by azc completion bash
_azc_completions() {
  local cur prev cmd
  cur="\${COMP_WORDS[COMP_CWORD]}"
  prev="\${COMP_WORDS[COMP_CWORD-1]}"
  cmd="\${COMP_WORDS[1]}"

  if [ "\$COMP_CWORD" -eq 1 ]; then
    COMPREPLY=($(compgen -W "${COMMANDS}" -- "\$cur"))
    return
  fi

  case "\$cmd" in
    scan)
      case "\$prev" in
        --subscription|-s) COMPREPLY=($(compgen -W "$(azc completion --list subscriptions 2>/dev/null)" -- "\$cur")); return;;
        --format|-f) COMPREPLY=($(compgen -W "table json" -- "\$cur")); return;;
        --region|-r) COMPREPLY=($(compgen -W "${regions}" -- "\$cur")); return;;
        --currency|-c) COMPREPLY=($(compgen -W "GBP USD EUR" -- "\$cur")); return;;
        --out|-o) COMPREPLY=($(compgen -f -- "\$cur")); return;;
      esac
      COMPREPLY=($(compgen -W "--subscription --resource-group --out --format --region --currency --group --no-group --no-ri ${GLOBAL_FLAGS}" -- "\$cur"))
      ;;
    price)
      case "\$prev" in
        --region|-r) COMPREPLY=($(compgen -W "${regions}" -- "\$cur")); return;;
        --os) COMPREPLY=($(compgen -W "linux windows" -- "\$cur")); return;;
        --currency|-c) COMPREPLY=($(compgen -W "GBP USD EUR" -- "\$cur")); return;;
      esac
      COMPREPLY=($(compgen -W "--region --os --currency ${GLOBAL_FLAGS}" -- "\$cur"))
      ;;
    compare)
      case "\$prev" in
        --subscription|-s) COMPREPLY=($(compgen -W "$(azc completion --list subscriptions 2>/dev/null)" -- "\$cur")); return;;
        --format|-f) COMPREPLY=($(compgen -W "table json" -- "\$cur")); return;;
        --region|-r) COMPREPLY=($(compgen -W "${regions}" -- "\$cur")); return;;
        --currency|-c) COMPREPLY=($(compgen -W "GBP USD EUR" -- "\$cur")); return;;
      esac
      COMPREPLY=($(compgen -W "--subscription --with --name --region --currency --format ${GLOBAL_FLAGS}" -- "\$cur"))
      ;;
    plan)
      case "\$prev" in
        --region|-r) COMPREPLY=($(compgen -W "${regions}" -- "\$cur")); return;;
        --currency|-c) COMPREPLY=($(compgen -W "GBP USD EUR" -- "\$cur")); return;;
        --format|-f) COMPREPLY=($(compgen -W "table json" -- "\$cur")); return;;
        --load|-l|--out|-o) COMPREPLY=($(compgen -f -- "\$cur")); return;;
      esac
      COMPREPLY=($(compgen -W "--interactive --load --last --region --currency --format --out --quick --no-ri ${GLOBAL_FLAGS}" -- "\$cur"))
      ;;
    config)
      if [ "\$COMP_CWORD" -eq 2 ]; then
        COMPREPLY=($(compgen -W "show get set add-sub remove-sub path" -- "\$cur"))
        return
      fi
      ;;
    doctor)
      COMPREPLY=($(compgen -W "${GLOBAL_FLAGS}" -- "\$cur"))
      ;;
    completion)
      if [ "\$COMP_CWORD" -eq 2 ]; then
        COMPREPLY=($(compgen -W "bash zsh fish" -- "\$cur"))
        return
      fi
      ;;
  esac
}

complete -F _azc_completions azc
`;
}

/**
 * Generate zsh completion script.
 */
function generateZsh() {
  const regions = REGIONS.map((r) => `'${r}'`).join(' ');
  return `#compdef azc
# azc zsh completion — generated by azc completion zsh

_azc() {
  local -a commands
  commands=(
    'scan:Scan a subscription and estimate costs'
    'price:Look up pricing for a single resource'
    'compare:Compare costs against a SKU change'
    'plan:Build a cost estimate interactively'
    'config:View and modify configuration'
    'doctor:Check your azc setup'
    'completion:Generate shell completions'
  )

  _arguments -C \\
    '1:command:->command' \\
    '*::arg:->args'

  case "\$state" in
    command) _describe 'azc commands' commands;;
    args)
      case "\$words[1]" in
        scan)
          _arguments \\
            '(-s --subscription)'{-s,--subscription}'[Subscription]:subscription:->subs' \\
            '(-g --resource-group)'{-g,--resource-group}'[Resource group]:group:' \\
            '(-o --out)'{-o,--out}'[Output file]:file:_files' \\
            '(-f --format)'{-f,--format}'[Format]:format:(table json)' \\
            '(-r --region)'{-r,--region}'[Region]:region:(${regions.replace(/'/g, '')})' \\
            '(-c --currency)'{-c,--currency}'[Currency]:currency:(GBP USD EUR)' \\
            '--no-ri[Skip RI recommendations]' \\
            '--no-group[Flat output]'
          ;;
        price)
          _arguments \\
            '1:query:' \\
            '(-r --region)'{-r,--region}'[Region]:region:(${regions.replace(/'/g, '')})' \\
            '--os[OS type]:os:(linux windows)' \\
            '(-c --currency)'{-c,--currency}'[Currency]:currency:(GBP USD EUR)'
          ;;
        compare)
          _arguments \\
            '(-s --subscription)'{-s,--subscription}'[Subscription]:subscription:->subs' \\
            '(-w --with)'{-w,--with}'[Change spec]:spec:' \\
            '--name[Resource name]:name:' \\
            '(-r --region)'{-r,--region}'[Region]:region:(${regions.replace(/'/g, '')})' \\
            '(-c --currency)'{-c,--currency}'[Currency]:currency:(GBP USD EUR)' \\
            '(-f --format)'{-f,--format}'[Format]:format:(table json)'
          ;;
        plan)
          _arguments \\
            '(-i --interactive)'{-i,--interactive}'[Interactive mode]' \\
            '(-l --load)'{-l,--load}'[Load estimate]:file:_files' \\
            '--last[Load most recent estimate]' \\
            '(-r --region)'{-r,--region}'[Region]:region:(${regions.replace(/'/g, '')})' \\
            '(-c --currency)'{-c,--currency}'[Currency]:currency:(GBP USD EUR)' \\
            '(-f --format)'{-f,--format}'[Format]:format:(table json)' \\
            '(-o --out)'{-o,--out}'[Output file]:file:_files' \\
            '(-q --quick)'{-q,--quick}'[Quick start template]' \\
            '--no-ri[Skip RI recommendations]'
          ;;
        config)
          _arguments '1:subcommand:(show get set add-sub remove-sub path)'
          ;;
        completion)
          _arguments '1:shell:(bash zsh fish)'
          ;;
      esac
      ;;
  esac

  case "\$state" in
    subs) compadd $(azc completion --list subscriptions 2>/dev/null);;
  esac
}

_azc "\$@"
`;
}

