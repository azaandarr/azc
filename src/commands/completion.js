/**
 * Register the completion command.
 */
module.exports = function registerCompletionCommand(program) {
  program
    .command('completion')
    .description('Generate shell completion scripts')
    .argument('<shell>', 'Shell type: bash, zsh, or fish')
    .option('--list <type>', 'List completable values (used by completion scripts)')
    .action((shell, opts) => {
      // Hidden --list flag for dynamic completion
      if (opts.list) {
        listValues(opts.list);
        return;
      }

      const shellLower = shell.toLowerCase();
      let script;

      switch (shellLower) {
        case 'bash':
          script = generateBash();
          break;
        case 'zsh':
          script = generateZsh();
          break;
        case 'fish':
          script = generateFish();
          break;
        default:
          process.stderr.write(chalk.red(`Unknown shell: ${shell}. Supported: bash, zsh, fish\n`));
          process.exit(1);
      }

      process.stdout.write(script);
      process.stderr.write(chalk.dim(`\n# Add to your shell profile:\n#   source <(azc completion ${shellLower})\n`));
    });
};
